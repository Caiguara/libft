#include "libft.h"
#include <limits.h>

static int	ft_isspace(char c)
{
	return ((c >= 9 && c <= 13) || (c == ' '));
}

int	ft_atoi(const char *nptr)
{
	int	i;
	int	sign;
	int	result;

	i = 0;
	sign = 1;
	result = 0;
	while (ft_isspace(nptr[i]))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		result = result * 10 + (nptr[i] - '0');
		i++;
		if ((result * sign) > INT_MAX)
			return (INT_MAX);
		if ((result * sign) < INT_MIN)
			return (INT_MIN);
	}
	return (sign * result);
}
